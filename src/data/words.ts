// 50語ずつのセットを生成
export function getWordSets(setSize: number): { start: number; end: number; label: string }[] {
  const sets: { start: number; end: number; label: string }[] = [];
  const totalWords = words.length;

  for (let i = 0; i < totalWords; i += setSize) {
    const start = i + 1;
    const end = Math.min(i + setSize, totalWords);
    sets.push({
      start,
      end,
      label: `No.${start} - No.${end}`
    });
  }

  return sets;
}

// 指定された範囲の単語を取得
export function getWordsInRange(start: number, end: number): Word[] {
  return words.filter(w => w.number >= start && w.number <= end);
}

// ランダムに単語をシャッフル
export function shuffleWords(wordList: Word[]): Word[] {
  const shuffled = [...wordList];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}
